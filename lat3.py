print("Apakah 5 lebih besar dari sama dengan -2?", 5 >= -2)
#menggunakan operator kurang dari sama dengan
print("Apakah 5 kurang dari sama dengan -2?", 5 <= -2)
#untuk operator kurang dari dan lebih dari akan menghasilkan keluaran true atau false
